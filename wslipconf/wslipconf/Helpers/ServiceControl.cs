        [StructLayout(LayoutKind.Sequential, Pack = 0)]
        public struct SERVICE_STATUS
        {
            public SERVICE_TYPES dwServiceType;
            public SERVICE_STATE dwCurrentState;
            public uint dwControlsAccepted;
            public uint dwWin32ExitCode;
            public uint dwServiceSpecificExitCode;
            public uint dwCheckPoint;
            public uint dwWaitHint;
        }



        [DllImport("advapi32.dll", EntryPoint = "OpenSCManagerW", CharSet = CharSet.Unicode)]
        public static extern IntPtr OpenSCManager(
          string lpMachineName,
          string lpDatabaseName,
          SCM_ACCESS dwDesiredAccess
        );

        [DllImport("advapi32.dll", EntryPoint = "OpenServiceW", CharSet = CharSet.Unicode)]
        public static extern IntPtr OpenService(
          IntPtr hSCManager,
          string lpServiceName,
          SERVICE_ACCESS dwDesiredAccess
        );
        [DllImport("advapi32.dll", SetLastError = true)]
        [return: MarshalAs(UnmanagedType.Bool)]
        public static extern bool ControlService(IntPtr hService, SERVICE_CONTROL dwControl, ref SERVICE_STATUS lpServiceStatus);


        [DllImport("advapi32.dll")]
        public static extern int CloseServiceHandle(IntPtr hSCObject);

        public static bool SendRefreshSignal()
        {
            bool retStatus = false;
            IntPtr hControl, hService;

            hControl = OpenSCManager(null, null, SCM_ACCESS.GENERIC_READ);

            if (hControl == IntPtr.Zero) return retStatus;


            hService = OpenService(hControl, "IpHlpSvc", SERVICE_ACCESS.SERVICE_PAUSE_CONTINUE | SERVICE_ACCESS.SERVICE_QUERY_STATUS);

            if (hService == IntPtr.Zero)
            {
                CloseServiceHandle(hControl);
                return retStatus;

            }

            SERVICE_STATUS status = new SERVICE_STATUS();
            
            retStatus = ControlService(hService, SERVICE_CONTROL.PARAMCHANGE, ref status);

            CloseServiceHandle(hService);
            CloseServiceHandle(hControl);

            return retStatus;
        }


